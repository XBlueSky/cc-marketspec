// Reads the exact @fontsource woff2 weights we use and emits data-URI
// @font-face rules to src/styles/fonts.css. No font CDN at runtime; the
// static build is self-contained. Run via `npm run fonts:inline`.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

const root = fileURLToPath(new URL('..', import.meta.url));
const faces = [
  { family: 'Newsreader', file: 'newsreader/files/newsreader-latin-400-normal.woff2', weight: 400, style: 'normal' },
  { family: 'Newsreader', file: 'newsreader/files/newsreader-latin-500-normal.woff2', weight: 500, style: 'normal' },
  { family: 'Newsreader', file: 'newsreader/files/newsreader-latin-500-italic.woff2', weight: 500, style: 'italic' },
  { family: 'Geist Sans', file: 'geist-sans/files/geist-sans-latin-400-normal.woff2', weight: 400, style: 'normal' },
  { family: 'Geist Sans', file: 'geist-sans/files/geist-sans-latin-500-normal.woff2', weight: 500, style: 'normal' },
  { family: 'Geist Sans', file: 'geist-sans/files/geist-sans-latin-600-normal.woff2', weight: 600, style: 'normal' },
  { family: 'Geist Mono', file: 'geist-mono/files/geist-mono-latin-400-normal.woff2', weight: 400, style: 'normal' },
];
const out = faces.map(({ family, file, weight, style }) => {
  const b64 = readFileSync(`${root}node_modules/@fontsource/${file}`).toString('base64');
  return `@font-face{font-family:'${family}';font-style:${style};font-weight:${weight};font-display:swap;src:url(data:font/woff2;base64,${b64}) format('woff2');}`;
}).join('\n');
writeFileSync(`${root}src/styles/fonts.css`, `/* GENERATED by scripts/gen-fonts.mjs — do not edit. */\n${out}\n`);
console.log(`Wrote ${faces.length} @font-face rules to src/styles/fonts.css`);
